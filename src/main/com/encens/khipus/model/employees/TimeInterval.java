package com.encens.khipus.model.employees;

import java.util.Date;

/**
 * @author
 * @version 3.2
 */
public class TimeInterval {
    private Date start;
    private Date end;

    public Date getStart() {
        return start;
    }

    public void setStart(Date start) {
        this.start = start;
    }

    public Date getEnd() {
        return end;
    }

    public void setEnd(Date end) {
        this.end = end;
    }
}
